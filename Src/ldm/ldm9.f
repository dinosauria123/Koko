C SUB PTORIC.FOR
      SUBROUTINE PTORIC
C
          IMPLICIT NONE
C
C       THIS IS SUBROUTINE PTORIC WHICH IMPLEMENTS THE TORIC,TR,TC,
C       RTORIC OR CTORIC COMMANDS AT THE CMD LEVEL
C       THE ONLY VALID QUALIFIERS ARE (BLANK) AND "ALL","OB" AND "OBJ"
C       THE NUMERIC ENTRY W1, IS THE SURFACE DESIGNATOR
C       FOR PRINTOUT OF ONE SURFACE'S DATA ONLY.
C       TR,TORIC,AND RTORIC PRODUCE RADIUS OUTPUT.
C       TC AND CTORIC PRODUCE CURVATURE OUTPUT.
C
          CHARACTER TYPE*10
C
          INTEGER
     6    RADIUS,TORCNT,I
C
          REAL*8 RADVAL,CURVAL
C
          INCLUDE 'datmai.inc'
          INCLUDE 'datlen.inc'
C
C               THIS IS A CMD LEVEL COMMAND ONLY
C
C               CHECK FOR ADDITIONAL INPUT AND
C               PRINT ERROR AND RETURN IF DISCOVERED.
C
          IF(SST.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
              OUTLYNE=
     1        'TORIC OUTPUT COMMANDS TAKE NO NUMERIC WORD #2 THROUGH #5'
              CALL SHOWIT(1)
              OUTLYNE='OR STRING INPUT'
              CALL SHOWIT(1)
              OUTLYNE='RE-ENTER COMMAND'
              CALL SHOWIT(1)
              CALL MACFAL
              RETURN
          END IF
          IF(SQ.EQ.1.AND.S1.EQ.1) THEN
              S1=0
              DF1=0
              W1=0.0D0
              OUTLYNE=
     1        'TORIC OUTPUT COMMANDS REQUIRE EITHER QUALIFIER OR'
              CALL SHOWIT(1)
              OUTLYNE='NUMERIC WORD #1 INPUT BUT NOT BOTH'
              CALL SHOWIT(1)
              OUTLYNE='RE-ENTER COMMAND'
              CALL SHOWIT(1)
              CALL MACFAL
              RETURN
          END IF
C
C       WHAT IF NO SURFACES EXIST
          IF(SYSTEM1(20).EQ.0.0D0) THEN
              OUTLYNE='NO TORICS EXIST'
              CALL SHOWIT(1)
              OUTLYNE='LENS SYSTEM HAS NO SURFACES'
              CALL SHOWIT(1)
              CALL MACFAL
              RETURN
          END IF
C       W1 DESIGNATES THE SURFACE FOR WHICH THE TORIC
C       DATA IS TO BE OUTPUT.
C       IF THE QUALIFIER "ALL" IS USED, THEN THE TILT/DEC DATA FOR
C       THE ENTIRE LENS IS PRINTED. IF THE QUALIFIER IS "OB" OR "OBJ"
C       THEN DATA FOR THE OBJECT SURFACE IS PRINTED.
C
          IF(WQ.EQ.'OB'.OR.WQ.EQ.'OBJ') THEN
C
C       THIS IS SAME AS SURFACE ZERO
              S1=1
              DF1=0
              SQ=0
              WQ='        '
              W1=0.0D0
          ELSE
C       WQ NOT OB OR OBJ
          END IF
          IF(SQ.EQ.1.AND.WQ.NE.'ALL') THEN
C
C       WE HAVE INVALID QUALIFIER
              OUTLYNE='INVALID QUALIFIER INPUT'
              CALL SHOWIT(1)
              OUTLYNE='RE-ENTER COMMAND'
              CALL SHOWIT(1)
              CALL MACFAL
              RETURN
          END IF
          IF(SQ.EQ.0) THEN
C       HANDEL AN INDIVIDUAL SURFACE INCLUDING "OB" AND "OBJ"
              IF(W1.LT.0.0D0) W1=SYSTEM1(20)+W1
              IF(DF1.EQ.1) W1=DBLE(INT(SYSTEM1(20)))
              SURF=INT(W1)
              IF(SURF.GT.INT(SYSTEM1(20)).OR.SURF.LT.0) THEN
                  OUTLYNE='SURFACE NUMBER BEYOND LEGAL RANGE'
                  CALL SHOWIT(1)
                  OUTLYNE='RE-ENTER COMMAND'
                  CALL SHOWIT(1)
                  CALL MACFAL
                  RETURN
              END IF
C SET RADIUS OR CURVATURE MODE AS APPROPRIATE
C
              IF(WC.EQ.'TR'.OR.WC.EQ.'TORIC'.OR.WC.EQ.'RTORIC') THEN
                  RADIUS=1
                  IF(ALENS(24,SURF).EQ.0.0D0) THEN
                      RADVAL=0.0D0
                  ELSE
                      RADVAL=1.0D0/(ALENS(24,SURF))
                  END IF
              ELSE
C       CURVATURE MODE
                  RADIUS=0
                  CURVAL=ALENS(24,SURF)
              END IF
C       SET TORIC TYPE
              IF(ALENS(23,SURF).EQ.0.0D0) THEN
C       NO TORIC DATA, WRITE MESSAGE AND RETURN
                  WRITE(OUTLYNE,110) SURF
                  CALL SHOWIT(0)
                  RETURN
              END IF
C       THERE IS A TORIC
              IF(ALENS(23,SURF).EQ.1.0D0) TYPE='Y-TORIC   '
              IF(ALENS(23,SURF).EQ.2.0D0) TYPE='X-TORIC   '
              IF(ALENS(36,SURF).NE.0.0D0.AND.
     1        ALENS(41,SURF).NE.0.0D0.OR.ALENS(36,SURF).NE.0.0D0.
     2        OR.ALENS(41,SURF).NE.0.0D0) THEN
C
C       THE TORIC CURVATURE HAS A CONIC OR ASPHERIC TERM
C       CHANGE TYPE NAME.
                  IF(ALENS(23,SURF).EQ.1.0D0) TYPE='Y-ANAMORPH'
                  IF(ALENS(23,SURF).EQ.2.0D0) TYPE='X-ANAMORPH'
              ELSE
C       SURFACES ARE JUST PLANE OLD TORICS.
              END IF
C
              IF(RADIUS.EQ.1) THEN
                  IF(HEADIN) WRITE(OUTLYNE,2000)
                  IF(HEADIN) CALL SHOWIT(0)
                  WRITE(OUTLYNE,200) SURF,TYPE,RADVAL
                  CALL SHOWIT(0)
                  RETURN
              ELSE
C               CURVATURE PRINT OUT
                  IF(HEADIN) WRITE(OUTLYNE,2001)
                  IF(HEADIN) CALL SHOWIT(0)
                  WRITE(OUTLYNE,200) SURF,TYPE,CURVAL
                  CALL SHOWIT(0)
              END IF
              RETURN
          ELSE
C*********************************************************************
C       SQ=1 AND ONLY ALLOWED WQ BY NOW IS "ALL"
C       HANDEL THE WHOLE LENS
              TORCNT=0
C
              DO 16 I=0,INT(SYSTEM1(20))
                  IF(ALENS(23,I).NE.0.0D0) THEN
                      TORCNT=TORCNT+1
                  ELSE
                  END IF
 16           CONTINUE
              IF(TORCNT.EQ.0) THEN
                  WRITE(OUTLYNE,100)
                  CALL SHOWIT(0)
                  RETURN
              ELSE
C       PROCEED WITH PRINTOUT
              END IF
              IF(WC.EQ.'TR'.OR.WC.EQ.'TORIC'.OR.WC.EQ.'RTORIC') THEN
                  RADIUS=1
                  WRITE(OUTLYNE,1000)
                  CALL SHOWIT(0)
                  WRITE(OUTLYNE,1500)
                  CALL SHOWIT(0)
                  WRITE(OUTLYNE,2000)
                  CALL SHOWIT(0)
              ELSE
                  RADIUS=0
                  WRITE(OUTLYNE,1001)
                  CALL SHOWIT(0)
                  WRITE(OUTLYNE,1500)
                  CALL SHOWIT(0)
                  WRITE(OUTLYNE,2001)
                  CALL SHOWIT(0)
              END IF
C
              DO 15 I=0,INT(SYSTEM1(20))
C
C       SET TORIC TYPE
C
                  IF(ALENS(23,I).EQ.0.0D0) THEN
C       NOT A TORIC OR ANAMORPH. JUMP TO 15 AND
C       GO TO THE NEXT SURFACE NUMBER
C       WITHOUT PRINTING DATA
                      GO TO 15
                  ELSE
C       PROCEED
                  END IF
                  IF(ALENS(23,I).EQ.1.0D0) TYPE='Y-TORIC   '
                  IF(ALENS(23,I).EQ.2.0D0) TYPE='X-TORIC   '
                  IF(ALENS(36,I).NE.0.0D0.AND.
     1            ALENS(41,I).NE.0.0D0.OR.ALENS(36,I).NE.0.0D0.
     2            OR.ALENS(41,I).NE.0.0D0) THEN
C
C       THE TORIC CURVATURE HAS A CONIC OR ASPHERIC TERM
C       CHANGE TYPE NAME.
                      IF(ALENS(23,I).EQ.1.0D0) TYPE='Y-ANAMORPH'
                      IF(ALENS(23,I).EQ.2.0D0) TYPE='X-ANAMORPH'
                  ELSE
C       SURFACES ARE JUST PLANE OLD TORICS.
                  END IF
C
                  IF(RADIUS.EQ.1) THEN
                      IF(ALENS(24,I).EQ.0.0D0) THEN
                          RADVAL=0.0D0
                      ELSE
                          RADVAL=1.0D0/(ALENS(24,I))
                      END IF
                      WRITE(OUTLYNE,200) I,TYPE,RADVAL
                      CALL SHOWIT(0)
                      GO TO 15
                  ELSE
C               CURVATURE PRINT OUT
                      CURVAL=ALENS(24,I)
                      WRITE(OUTLYNE,200) I,TYPE,CURVAL
                      CALL SHOWIT(0)
                  END IF
C
 15           CONTINUE
          END IF
C
 200      FORMAT(I3,1X,A10,2X,G13.6)
C
 110      FORMAT('SURF',1X,I3,1X,
     1    ':NO TORIC OR ANAMORPHIC DATA')
 100      FORMAT('NO TORIC OR ANAMORPHIC DATA')
 1000     FORMAT('TORIC/ANAMORPHIC DATA (RADIUS MODE)')
 1001     FORMAT('TORIC/ANAMORPHIC DATA (CURVATURE MODE)')
 2000     FORMAT('SURF',2X,'TYPE',8X,'RADIUS')
 2001     FORMAT('SURF',2X,'TYPE',8X,'CURVATURE')
 1500     FORMAT(1X)
          RETURN
      END
